package JavaPractice;

public class DataTypesConcepts {


	
	public static void main(String[] args) {

		//There are two types of data types ie. primitive and non primitive
		
		//Data types specify/assign different sizes and values to store in the variable.
		// primitve data types=> byte, short, int, float, double , char, boolean and long.
		// Non primitive data type => It includes classes, interface and arrays.
		
		//Boolean ==> This type of data only used to store only two possible values: true and false.
		//Syntax.
		boolean b = false;
		
		// byte ==> This data type  is used to save memory in large arrays. Its range is -127 to 128.it is 8 bit signed
		
		// syntax
		 byte b1 = 10;
		 
		 
		 // Short data type==> This data type is also used to save memory in large arrays. its range is -32768 to 32767.it is 16 bit signed.
		 
		 // syntax
		 short s = 100;
		 
		 
		 // int ==>  Generally we used this data type. its range is - 2,147,483,648 (-2^31) to 2,147,483,647 (2^31 -1). it is 32 bit signed
		 
		 //syntax
		 int i = 1000;
		 
		// long==> This data type is used when require large value than int data type. it is 64 bit signed. its range -9,223,372,036,854,775,808(-2^63) to 9,223,372,036,854,775,807(2^63 -1)
		 
		 long l = 100000000l;
		 
		 // float ==> This data type is used for decimal value . it is 32 bit. 
		 
		 //syntax 
		 float f = 10.100f;
		 
		 
		 //double ==> This data type is also used for decimal value like float. it is 64 bit.
		 //syntax
		 double d = 10.10;
		
		 // char ==> This data type is used to store characters. it is 16 bit.
		 	
	}

}
