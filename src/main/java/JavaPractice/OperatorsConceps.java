package JavaPractice;

public class OperatorsConceps {

	public static void main(String args[]){
		
		//Operators in java is used to perform the operation. example==> +, -, % , / ,* etc.
		
		//Important types of operators only.
		// unary operator  2. Arithmatic operator  3. Relational operator   4. Logical operator.
		
		//Unary operator
		// exp++ , ++exp,c --exp and exp--. 
		// Use to increment / decrement the value by one.
		

		int i=10;
		System.out.println(i++);
		System.out.println(++i);
		System.out.println(i--);
		System.out.println(--i);
		
		// Arithmatic operator.
		// java arithmatic operators is used to perform addition , subtraction, multiplication and division.
		
		int a = 10, b = 20;
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(b/a);
		
		
		//Logical operator.
		// logical operator is used to check the conditions.
		
		int c=10,  d=20, e=30;
		
		
	}
}
