package JavaPractice;

public class WhileloopPrac {

	public static void main(String[] args) {

		int i = 0;
		while (i < 6) {

			System.out.println(i);
			i++;
		}
		
		int j =3 ;
		
		while (j>0) {

			System.out.println(j);
			j--;
		}
		int k =0;
		do {
		
				System.out.println(k);
				k++;
			
		}while(k<5);	
	
	}

}
