package JavaPractice;

public class SwitchPrac {

	public static void main(String [] args){
		
		int a =2;
		
		switch(a){
		
		case 1:
			System.out.println("not matched ");
			
		case 2 :
			
			System.out.println(" matched ");
		break;
		case 3 :
			
			System.out.println(" matched 3 ");
		
		}
		
	int b =6;
		
		switch(b){
		
		case 1:
			System.out.println("not matched ");
			break;
		case 2 :
			
			System.out.println(" matched ");
		break;
		case 3 :
			
			System.out.println(" matched 3 ");
		break;
		
		default:
			System.out.println(" Not value matched ");
		
		}
		
		
	}
	
}
